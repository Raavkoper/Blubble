void	hook(void *param)
{
	if (mlx_is_key_down(param, MLX_KEY_ESCAPE))
		mlx_close_window(param);
}

int	main(int argc, char **argv)
{
	t_data	data;

	if (argc == 2)
		read_map(&data, argv[1]);
	else
		printf("no given map input ofz\n");
	double posX = 22;
	double posY = 12;
	double dirX	= -1;
	double dirY = 0;
	double planeX = 0;
	double planeY = 0.66;
	double time = 0;
	double oldtime = 0;
	int x = 0;
	double cameraX;
	double rayDirX;
	double rayDirY;
	// print_map(data.map);
	data.mlx = mlx_init(screenWidth, screenHeight, "Blubble", true);
	if (!data.mlx)
		exit(EXIT_FAILURE);
	data.g_img = mlx_new_image(data.mlx, 128, 128);
	memset(data.g_img->pixels, 255, data.g_img->width * data.g_img->height \
	* sizeof(int));
	while (x < mapWidth)
	{
		cameraX = 2 * x / mapWidth - 1;
		rayDirX = dirX + planeX * cameraX;
		rayDirY = dirX + planeX * cameraX;
		x++;
	}
	mlx_loop_hook(data.mlx, &hook, data.mlx);
	mlx_loop(data.mlx);
	mlx_terminate(data.mlx);
	return (EXIT_SUCCESS);
}
